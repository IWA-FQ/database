CREATE TABLE "recommandation" (
                                           "id_user" varchar,
                                           "id_offer" varchar,
                                           "score" int,
                                           PRIMARY KEY ("id_user", "id_offer")
);

CREATE TABLE "recruiting" (
                                       "offer" int,
                                       "employee" varchar,
                                       "date" date,
                                       "employee_rate" int,
                                       "employer_rate" int,
                                       PRIMARY KEY ("offer", "employee")
);

CREATE TABLE "apply" (
                                  "offer" int,
                                  "applicant" varchar,
                                  "date" date,
                                  PRIMARY KEY ("offer", "applicant")
);

CREATE TABLE "offer" (
                                  "id" INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                  "title" varchar,
                                  "description" varchar,
                                  "start_date" date,
                                  "end_date" date,
                                  "city" varchar,
                                  "city_code" int,
                                  "nb_positions" int,
                                  "salary" int,
                                  "advantage" varchar,
                                  "employer" varchar
);

ALTER TABLE "apply" ADD FOREIGN KEY ("offer") REFERENCES "offer" ("id");

ALTER TABLE "recruiting" ADD FOREIGN KEY ("offer") REFERENCES "offer" ("id");